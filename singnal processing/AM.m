% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
ka=0.8;

c=get(handles.edit1,'string');
d=get(handles.edit2,'string');
a=str2double(c); %amplitude
f=str2double(d); %frequncy

Am=a;
Fm=f;
t=0:0.001:1;
mt=Am*sin(2*pi*Fm*t);

q=get(handles.edit3,'string');
w=get(handles.edit4,'string');
ca=str2double(q); %amplitude
cf=str2double(w); %frequncy

Ac=ca;
Fc=cf;
ct=Ac*cos(2*pi*Fc*t);

st=(1+ka.*mt).*ct;

%subplot(3,1,1)
axes(handles.axes1)
plot(t,mt)
title('Message Signal m(t)');
xlabel('time t')
ylabel('amplitude')
guidata(hObject,handles)

%subplot(3,1,2)
axes(handles.axes2)
plot(t,ct)
title('Carrier Signal c(t)');
xlabel('time t')
ylabel('ampilitude')
guidata(hObject,handles)

%subplot(3,1,3)
axes(handles.axes3)
plot(t,st)
title('Moduleted Signal s(t)');
xlabel('time t')
ylabel('ampilitude')
guidata(hObject,handles)




